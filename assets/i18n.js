/* ── Apply translations to the page ── */
window.applyTranslations = function (lang) {
  const meta = window.LANG_META[lang] || window.LANG_META.en;
  const T    = window.TRANSLATIONS[lang] || window.TRANSLATIONS.en;

  /* Document direction + lang attribute */
  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("dir",  meta.dir);
  document.documentElement.setAttribute("data-lang", lang);

  /* text content */
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const v = T[el.dataset.i18n];
    if (v !== undefined) el.textContent = v;
  });

  /* innerHTML (supports <em>, <b>, etc.) */
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const v = T[el.dataset.i18nHtml];
    if (v !== undefined) el.innerHTML = v;
  });

  /* placeholder */
  document.querySelectorAll("[data-i18n-ph]").forEach(el => {
    const v = T[el.dataset.i18nPh];
    if (v !== undefined) el.placeholder = v;
  });

  /* title */
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    const v = T[el.dataset.i18nTitle];
    if (v !== undefined) el.title = v;
  });

  /* aria-label */
  document.querySelectorAll("[data-i18n-aria]").forEach(el => {
    const v = T[el.dataset.i18nAria];
    if (v !== undefined) el.setAttribute("aria-label", v);
  });

  /* Update language picker button */
  const flagEl = document.getElementById("langFlag");
  const codeEl = document.getElementById("langCode");
  if (flagEl) flagEl.textContent = meta.flag;
  if (codeEl) codeEl.textContent = meta.code;

  /* Mark active item in dropdown */
  document.querySelectorAll(".lang-option").forEach(li => {
    li.setAttribute("aria-selected", li.dataset.lang === lang ? "true" : "false");
  });
};

/* ── Detect preferred language ── */
window.detectLang = function () {
  const saved = localStorage.getItem("cloudping.lang");
  if (saved && window.TRANSLATIONS[saved]) return saved;
  const browser = (navigator.language || "en").split("-")[0].toLowerCase();
  if (window.TRANSLATIONS[browser]) return browser;
  return "en";
};
